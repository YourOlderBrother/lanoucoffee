package com.lanou.model;

public class OrderDetails {
	private Integer detId;//订单明细编号
	private Integer ordId;//订单编号
	private Integer cofId;//商品ID
	private Integer Number;//商品数量
	
	public OrderDetails() {
		super();
	}

	public OrderDetails(Integer ordId, Integer cofId, Integer number) {
		super();
		this.ordId = ordId;
		this.cofId = cofId;
		Number = number;
	}

	public Integer getDetId() {
		return detId;
	}

	public void setDetId(Integer detId) {
		this.detId = detId;
	}

	public Integer getOrdId() {
		return ordId;
	}

	public void setOrdId(Integer ordId) {
		this.ordId = ordId;
	}

	public Integer getCofId() {
		return cofId;
	}

	public void setCofId(Integer cofId) {
		this.cofId = cofId;
	}

	public Integer getNumber() {
		return Number;
	}

	public void setNumber(Integer number) {
		Number = number;
	}

	
	
}
